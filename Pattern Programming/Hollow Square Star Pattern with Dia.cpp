#include<bits/stdc++.h>
using namespace std;

#define fastio() ios_base::sync_with_stdio(false);cin.tie(NULL);cout.tie(NULL)
#define nline "\n";

int main() {

	fastio();

	for(int i = 0; i < 5; ++i)
	{
		for(int j = 0; j < 5; ++j)
		{
			if((i > 0 && i < 4) && (j > 0 && j < 4) && (!(i == j || i + j == 4)))
			{
				cout << " ";
			}
			else
				cout << "*";
		}
		cout << nline;
	}
}
